import 'dart:io';

void main() {
  double age = 22;
  age += 1; //here the value of age is age + 1
  print("the value of age after addition is $age");
  age -= 1; //here the value of age is age -1
  print("the value of age after subtraction is $age");
  age *= 2; //here the value of age is age * 2
  print("the value of age after multiplication is $age");
  age /= 2; //here the value of age age /2
  print("the value of age after division is $age");
}
